package com.company;

public class cwh_23_for_loop {
    public static void main(String[] args) {
        for(int i=1; i<=10; i++){
            System.out.println(i);
        }
        int a = 5;
        System.out.println("print odd number 0 to 10");
        for(int i=0; i<a; i++){
            System.out.println(2*i+1);
        }
    }
}

